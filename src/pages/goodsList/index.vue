<script>
export default {
  data() {
    // data 是往自己组件内部，挂载一些私有数据的
    return {
      pageindex: 1, // 分页的页数
      goodslist: [] // 存放商品列表的数组
    };
  },
  created() {
    this.getGoodsList();
  },
  mounted() {
    // console.log(this.$refs)
    this.$refs.sc.triggerPullToRefresh();
  },
  methods: {
    getGoodsList(refresh) {
      // 可能需要拼接, 也有可能需要覆盖
      // 获取商品列表
      return this.$http
        .get("getgoods?pageindex=" + this.pageindex)
        .then(result => {
          if (refresh) {
            this.goodslist = result.body.message;
          } else {
            this.goodslist = this.goodslist.concat(result.body.message);
          }
          // 下拉刷新组件都会提供一个api, 调用就可以停止下拉刷新
          // 手动停止下拉刷新
          // this.$refs.sc.finishPullToRefresh()
        });
    },
    goDetail(id) {
      // 使用JS的形式进行路由导航

      // 注意： 一定要区分 this.$route 和 this.$router 这两个对象，
      // 其中： this.$route 是路由【参数对象】，所有路由中的参数， params, query 都属于它
      // 其中： this.$router 是一个路由【导航对象】，用它 可以方便的 使用 JS 代码，实现路由的 前进、后退、 跳转到新的 URL 地址

      this.$router.push("/home/goodsInfo/" + id);

      // 1. 最简单的
      // this.$router.push("/home/goodsinfo/" + id);
      // 2. 传递对象
      // this.$router.push({ path: "/home/goodsinfo/" + id });
      // 3. 传递命名的路由
      // this.$router.push({ name: "goodsinfo", params: { id } });
    },
    refresh() {
      setTimeout(() => {
        // console.log('我在刷新...')
        // 刷新第一页的数据
        // 1. 清空原数据
        // this.goodslist = []
        // 2. 重置索引pageindex 为 1
        this.pageindex = 1;
        // 3. 获取第一页的数据并渲染
        // 4. 结束下拉刷新
        this.getGoodsList(true).then(() => this.$refs.sc.finishPullToRefresh());
        // 获取商品列表
      }, 2000);
    },
    infinite() {
      setTimeout(() => {
        this.pageindex++;
        this.getGoodsList().then(() =>
          this.$refs.sc.finishInfinite(this.goodslist.length === 15)
        );
      }, 2000);
      // console.log('我在上拉加载更多...')
    }
  }
};
</script>

