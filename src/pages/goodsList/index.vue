    loadingLayerColor="#0094ff"
    :snapping="true"
    noDataText="客官莫得了~~"
    refreshText="用点力往下拉~~"
    :on-refresh="refresh"
    :on-infinite="infinite"
  >
    <div class="goods-list">
      <!-- <router-link class="goods-item" v-for="item in goodslist" :key="item.id" :to="'/home/goodsinfo/' + item.id" tag="div">
